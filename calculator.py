def add(a, b):
    return a + b


if __name__ == "__main__":
    print("5 + 3 =", add(5, 3))


def subtract(a, b):
    return a - b
if __name__ == "__main__":
    print("5 - 3 =", subtract(5, 3))

def multiply(a, b):
    return a * b
if __name__ == "__main__":
    print("5 * 3 =", multiply(5, 3))
    